package br.com.aula.operadores;

public class Operadores {
	
	public static void main(String[] args) {
		
		//operadores basicos matematicos
		System.out.println("operadores basicos matematicos");
		System.out.println(1+2);//soma
		System.out.println(2-1);//subtração
		System.out.println(1*2);//multiplicação
		System.out.println(2/2);//divisão
		System.out.println("----------------------------------");
		
		//comparativos
		System.out.println("comparativos");
		System.out.println(2>1);// 2 é maior que 1?
		System.out.println(1<2);//1 é menor que 2?
		System.out.println(2>=1);//2 é maior ou igual a 1?
		System.out.println(2<=1);//2 é menor ou igual a 1?
		System.out.println(2==1);//2 é igual a 1?
		System.out.println("----------------------------------");
		
	}

}
